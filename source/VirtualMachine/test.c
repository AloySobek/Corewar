#include <stdio.h>

int main(void)
{
	int test;
	int i;
	char new[4] = {100, 100, 100, 100};

	test = 0;
	i = 0;
	while (i < 4)
	{
		test |= new[i] << (sizeof(int) - i + 1) * 8;
		++i;
	}
	printf("%d\n", test);
	return (0);
}